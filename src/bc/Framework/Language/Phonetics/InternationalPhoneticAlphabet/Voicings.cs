namespace bc.Framework.Language.Phonetics.InternationalPhoneticAlphabet
{
    /// <summary>
    /// Characterizations of sounds related to how the sound is vocally produced 
    /// </summary>
    public enum Voicings
    {
        /// <summary>
        /// the property of being pronounced by the vibration of the larynx
        /// </summary>
        Voiced,

        /// <summary>
        /// the property of being pronounced without the larynx vibrating
        /// </summary>
        Voiceless
    }
}